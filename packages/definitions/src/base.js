/**
* Create a Zod schema for a cell value based on column config.
* All cell schemas are nullable (empty cells are valid).
*/
function createCellSchema(config) {
	switch (config.type) {
		case "string": return z.string().nullable();
		case "number": return z.number({ message: "Must be a valid number" }).nullable();
		case "boolean": return z.boolean().nullable();
		case "date": return z.iso.date().nullable();
		case "select":
			if (config.options.length === 0) return z.string().nullable();
			return z.enum(config.options.map((o) => o.id)).nullable();
	}
}
/**
* Create a Zod schema for an entire row based on column definitions.
* Each column becomes a field in the object schema.
*/
function createRowSchema(columns) {
	const shape = {};
	for (const column of columns) shape[column.id] = createCellSchema(column.config).describe(column.name);
	return z.object(shape);
}
/**
* Convert row schema to JSON Schema for AI structured outputs.
* Use this when generating prompts for LLMs that support structured outputs.
*/
function rowSchemaToJsonSchema(columns) {
	return z.toJSONSchema(createRowSchema(columns));
}

